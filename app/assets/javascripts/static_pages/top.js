function clickBtnOthers(){
  const another_form = document.getElementById("another_form");
  const another_button = document.getElementById("top-another-button-wrapper");
  another_form.style.display ="block";
  another_button.style.display ="none";
}
function clickBtnOthersHide(){
const another_form = document.getElementById("another_form");
const another_button = document.getElementById("top-another-button-wrapper");
  another_form.style.display ="none";
  another_button.style.display ="block";
}

